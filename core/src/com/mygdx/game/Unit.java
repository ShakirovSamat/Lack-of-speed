package com.mygdx.game;

public class Unit {
    public int width;
    public int height;
    public int xPosition;
    public int yPosition;

    public Unit(int width, int height, int xPosition, int yPosition) {
        this.width = width;
        this.height = height;
        this.xPosition = xPosition;
        this.yPosition = yPosition;
    }
}
